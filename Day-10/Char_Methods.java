package day10;

public class Char_Methods {
    public static void main(String[] args){

        //to check the character of a string

        String j="Hello";
        char ch=j.charAt(2);
        System.out.println(ch);

        //to check the full string using char method array
        String h="Hello";
        char [] chars=h.toCharArray();
        System.out.println(chars);
    }
}
